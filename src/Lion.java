public class Lion implements Animal{
    @Override
    public void breath() {
        System.out.println("Lion is breathing");
    }

    @Override
    public void eat() {
        System.out.println("Lion is eating");

    }

    @Override
    public void sleep() {
        System.out.println("Lion is sleeping");

    }

    @Override
    public void move() {
        System.out.println("Lion is moving");

    }
}
